func TestList(t *testing.T) {
	templates := List()
	if len(templates) == 0 {
		t.Error("List() returned empty")
	}

	// Verify all listed templates exist
	for _, name := range templates {
		_, err := GetRaw(name)
		if err != nil {
			t.Errorf("Listed template %s not found: %v", name, err)
		}
	}
}

