func TestGetRaw(t *testing.T) {
	tests := []struct {
		name     TemplateName
		contains string
	}{
		{RepositoriesBasic, "kind: repositories"},
		{RepositoriesChild, "parent:"},
		{RepositoriesBootstrap, "Generated by gz-git"},
		{WorkspaceWorkstation, "kind: workspace"},
		{WorkspaceForge, "source:"},
		{Profile, "provider:"},
	}

	for _, tt := range tests {
		t.Run(string(tt.name), func(t *testing.T) {
			content, err := GetRaw(tt.name)
			if err != nil {
				t.Fatalf("GetRaw(%s) error: %v", tt.name, err)
			}
			if !strings.Contains(string(content), tt.contains) {
				t.Errorf("GetRaw(%s) missing expected content %q", tt.name, tt.contains)
			}
		})
	}
}

func TestRender_RepositoriesBasic(t *testing.T) {
	data := RepositoriesBasicData{
		Name:        "test-devbox",
		Team:        "backend",
		Description: "Test devbox",
		Strategy:    "pull",
		Parallel:    10,
		Repositories: []RepoData{
			{Name: "repo-a", URL: "git@github.com:org/repo-a.git", Path: "./repo-a"},
			{Name: "repo-b", URL: "git@github.com:org/repo-b.git", Path: "./repo-b", Branch: "develop"},
		},
	}

	result, err := Render(RepositoriesBasic, data)
	if err != nil {
		t.Fatalf("Render error: %v", err)
	}

	expected := []string{
		"version: 1",
		"kind: repositories",
		"name: test-devbox",
		"strategy: pull",
		"parallel: 10",
		"name: repo-a",
		"name: repo-b",
		"branch: develop",
	}

	for _, exp := range expected {
		if !strings.Contains(result, exp) {
			t.Errorf("Render result missing %q\nGot:\n%s", exp, result)
		}
	}
}

